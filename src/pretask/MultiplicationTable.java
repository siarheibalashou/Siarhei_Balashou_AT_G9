package pretask;

public class MultiplicationTable {
    public static void main(String[] args) {
        for (int fiveMultiplier = 1; fiveMultiplier <= 10; fiveMultiplier++) {
            int result = fiveMultiplier * 5;
            System.out.println("5 x " + fiveMultiplier +" = "+result);
        }
    }
}
